// Helper function to get device locale with fallback
const getDeviceLocale = (): string => {
  try {
    if (Platform.OS !== 'web') {
      // Use react-native-localize for mobile
      const locales = getLocales();
      if (locales && locales.length > 0) {
        return locales[0].languageCode;
      }
    }
    
    // Fallback to expo-localization
    const deviceLocale = Localization.locale || 'en';
    return deviceLocale.split('-')[0]; // Get language code only
  } catch (error) {
    console.warn('Error getting device locale:', error);
    return 'en';
  }
};

// Helper function to find best matching language
const findBestLanguageMatch = (preferredLanguage: string): string => {
  const supportedCodes = SUPPORTED_LANGUAGES.map(lang => lang.code);
  
  // Direct match
  if (supportedCodes.includes(preferredLanguage)) {
    return preferredLanguage;
  }
  
  // Try to find partial match (e.g., 'en-US' -> 'en')
  const languageCode = preferredLanguage.split('-')[0];
  if (supportedCodes.includes(languageCode)) {
    return languageCode;
  }
  
  // Default fallback
  return 'en';
};

// Initialize i18n
const i18n = new I18n(translations);

// Set initial locale
const deviceLocale = getDeviceLocale();
const initialLocale = findBestLanguageMatch(deviceLocale);

i18n.locale = initialLocale;
i18n.fallbacks = true;
i18n.defaultLocale = 'en';

// Enable fallbacks for missing translations
i18n.enableFallback = true;

// Helper function to get language info
export const getLanguageInfo = (code: string) => {
  return SUPPORTED_LANGUAGES.find(lang => lang.code === code) || SUPPORTED_LANGUAGES[0];
};

// Helper function to check if language is RTL
export const isRTL = (languageCode: string): boolean => {
  const language = getLanguageInfo(languageCode);
  return language?.rtl || false;
};

// Helper function to get greeting based on time
export const getTimeBasedGreeting = (languageCode: string): string => {
  const hour = new Date().getHours();
  
  if (hour < 12) {
    return i18n.t('dashboard.goodMorning', { locale: languageCode });
  } else if (hour < 17) {
    return i18n.t('dashboard.goodAfternoon', { locale: languageCode });
  } else {
    return i18n.t('dashboard.goodEvening', { locale: languageCode });
  }
};

export default i18n;